// 根据分类获取推荐贴纸
export const getCategoryStickers = (category) => {
  const categoryStickerMap = {
    '效率提升': [
      stickers.weeklyReport,
      stickers.growthChart,
      stickers.checklist,
      stickers.robotAssistant
    ],
    '生活灵感': [
      stickers.coffeeCup,
      stickers.pottedPlant,
      stickers.forkIcon,
      stickers.travelPhoto
    ],
    '学习成长': [
      stickers.stackOfBooks,
      stickers.mindMap,
      stickers.openBook,
      stickers.pencil
    ],
    '娱乐消遣': [
      stickers.gumballMachine,
      stickers.plushLamb,
      stickers.giftBox,
      stickers.crystalBall
    ],
    '创意脑洞': [
      stickers.pinkSparkle,
      stickers.yellowSparkle,
      stickers.paperAirplane,
      stickers.envelope
    ]
  };

  return categoryStickerMap[category] || [stickers.yellowBurst, stickers.pinkHeart];
};

// 获取随机贴纸
export const getRandomSticker = () => {
  const allStickers = Object.values(stickers);
  return allStickers[Math.floor(Math.random() * allStickers.length)];
};